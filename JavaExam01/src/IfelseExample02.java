public class IfelseExample02 {
    public static void main(String[] args) {
        int score = 22;

        if(score > 90) {
            System.out.println("90보다 큼");
            System.out.println("A");
        } else {
            System.out.println("90보다 작음");
            System.out.println("B");
        }

    }
}